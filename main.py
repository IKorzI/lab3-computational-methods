import math

def calculate_erlang_c(lam, mu, m):
    """Обчислює ймовірність черги за формулою Ерланга С."""
    rho = lam / (m * mu)
    if rho >= 1:
        return "Система перевантажена"

    # Обчислення P0
    sum_p = sum(((m * rho)**i) / math.factorial(i) for i in range(m))
    p0_inv = sum_p + (((m * rho)**m) / math.factorial(m)) * (1 / (1 - rho))
    p0 = 1 / p0_inv

    # Обчислення ймовірності очікування (P_q)
    p_q = (((m * rho)**m) / math.factorial(m)) * (1 / (1 - rho)) * p0
    return p_q

def main():
    # Вхідні дані з Case 3 (Максимальне навантаження на сервіси)
    lam = 1800  # Інтенсивність потоку (запитів/сек)
    m = 20      # Кількість серверів
    mu = 100    # Інтенсивність обслуговування (запитів/сек)

    # Розрахунок метрик
    rho = lam / (m * mu)
    p_q = calculate_erlang_c(lam, mu, m)
    w_q = p_q / (m * mu - lam)
    w = w_q + (1 / mu)

    # Вивід результатів
    print("--- Результати аналітичної моделі M/M/m ---")
    print(f"Завантаження системи (rho): {rho:.3f} ({rho*100:.1f}%)")
    print(f"Ймовірність черги (P_q): {p_q:.3f}")
    print(f"Середній час у черзі (W_q): {w_q*1000:.3f} мс")
    print(f"Середній час у системі (W): {w*1000:.3f} мс")

if __name__ == "__main__":
    main()
